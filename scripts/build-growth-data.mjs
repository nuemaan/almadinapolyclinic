// Build prescription/growth-data.js from the OFFICIAL WHO LMS source files.
//
// Sources (public, WHO-authored):
//   0–5 yrs  — WHO Child Growth Standards 2006:
//     https://github.com/WorldHealthOrganization/anthro  (data-raw/growthstandards)
//       weianthro.txt (weight-for-age), lenanthro.txt (length/height-for-age),
//       bmianthro.txt (BMI-for-age) — age in DAYS.
//   5–19 yrs — WHO Reference 2007:
//     https://github.com/WorldHealthOrganization/anthroplus (data-raw/growthstandards)
//       wfawho2007.txt, hfawho2007.txt, bfawho2007.txt — age in MONTHS.
//
// Output: window.WHO_GROWTH = { wfa|hfa|bfa: { "1"(boys)|"2"(girls): {start, L[], M[], S[]} } }
// indexed by completed month (index = month - start). The browser interpolates
// L,M,S between months and computes z = ((y/M)^L - 1) / (L*S) with the WHO tail
// correction. Regenerate with:  node scripts/build-growth-data.mjs
//
// Run with Node 18+ (global fetch). Validates anchor medians before writing.

import { writeFileSync } from 'node:fs';

const A05 = 'https://raw.githubusercontent.com/WorldHealthOrganization/anthro/master/data-raw/growthstandards';
const A519 = 'https://raw.githubusercontent.com/WorldHealthOrganization/anthroplus/master/data-raw/growthstandards';
const DAYS_PER_MONTH = 365.25 / 12; // 30.4375

async function load(url) {
  const txt = await (await fetch(url)).text();
  const lines = txt.trim().split(/\r?\n/);
  const head = lines[0].split(/\s+/);
  const idx = (n) => head.indexOf(n);
  const [si, ai, li, mi, sci] = [idx('sex'), idx('age'), idx('l'), idx('m'), idx('s')];
  return lines.slice(1).map((ln) => {
    const c = ln.split(/\s+/);
    return { sex: +c[si], age: +c[ai], L: +c[li], M: +c[mi], S: +c[sci] };
  });
}

// 0–5 file is daily → pick the row for each completed month (day = round(m*30.4375)).
function monthlyFromDaily(rows, sex, maxMonth) {
  const bySexDay = new Map();
  for (const r of rows) if (r.sex === sex) bySexDay.set(r.age, r);
  const out = [];
  for (let m = 0; m <= maxMonth; m++) {
    const r = bySexDay.get(Math.round(m * DAYS_PER_MONTH));
    if (!r) throw new Error(`missing day for month ${m}`);
    out.push(r);
  }
  return out;
}
// 2007 file is already monthly.
function monthlyFrom2007(rows, sex, fromMonth, toMonth) {
  const bySexMonth = new Map();
  for (const r of rows) if (r.sex === sex) bySexMonth.set(r.age, r);
  const out = [];
  for (let m = fromMonth; m <= toMonth; m++) {
    const r = bySexMonth.get(m);
    if (r) out.push(r);
  }
  return out;
}

function pack(series) {
  // series: array of {L,M,S} starting at month 0
  return {
    start: 0,
    L: series.map((r) => +r.L),
    M: series.map((r) => +r.M),
    S: series.map((r) => +r.S),
  };
}

async function main() {
  const [wei, len, bmi, wfa7, hfa7, bfa7] = await Promise.all([
    load(`${A05}/weianthro.txt`), load(`${A05}/lenanthro.txt`), load(`${A05}/bmianthro.txt`),
    load(`${A519}/wfawho2007.txt`), load(`${A519}/hfawho2007.txt`), load(`${A519}/bfawho2007.txt`),
  ]);

  const build = (daily, wk2007, maxMonth, max2007) => {
    const res = {};
    for (const sex of [1, 2]) {
      const a = monthlyFromDaily(daily, sex, maxMonth);          // 0..60
      const b = monthlyFrom2007(wk2007, sex, maxMonth + 1, max2007); // 61..max
      res[sex] = pack(a.concat(b));
    }
    return res;
  };

  const WHO_GROWTH = {
    wfa: build(wei, wfa7, 60, 120),   // weight-for-age 0–120 mo (10y)
    hfa: build(len, hfa7, 60, 228),   // height-for-age 0–228 mo (19y)
    bfa: build(bmi, bfa7, 60, 228),   // BMI-for-age   0–228 mo (19y)
  };

  // ---- sanity-check anchor medians against well-known WHO values ----
  // (values are copied verbatim from source; these guard against gross errors)
  const near = (a, b, tol, what) => { if (Math.abs(a - b) > tol) throw new Error(`anchor off: ${what} got ${a} exp ~${b}`); };
  near(WHO_GROWTH.wfa[1].M[0], 3.3464, 0.001, 'boys birth weight');   // exact 2006 day 0
  near(WHO_GROWTH.wfa[2].M[0], 3.23,  0.05, 'girls birth weight ~3.2');
  near(WHO_GROWTH.hfa[1].M[0], 49.8842, 0.001, 'boys birth length');  // exact 2006 day 0
  near(WHO_GROWTH.wfa[1].M[60], 18.33, 0.05, 'boys 5y weight ~18.3');
  near(WHO_GROWTH.hfa[1].M[60], 110.0, 0.6, 'boys 5y height ~110');
  near(WHO_GROWTH.hfa[1].M[228], 176.1, 1.0, 'boys 19y height ~176');
  if (WHO_GROWTH.wfa[1].M.length !== 121) throw new Error('wfa length ' + WHO_GROWTH.wfa[1].M.length);
  if (WHO_GROWTH.hfa[1].M.length !== 229) throw new Error('hfa length ' + WHO_GROWTH.hfa[1].M.length);

  const banner = `// AUTO-GENERATED by scripts/build-growth-data.mjs — DO NOT EDIT BY HAND.\n` +
    `// Official WHO Child Growth Standards (2006, 0–5y) + WHO Reference 2007 (5–19y).\n` +
    `// LMS by completed month; sex 1=boys, 2=girls. wfa 0–120mo, hfa/bfa 0–228mo.\n`;
  writeFileSync(new URL('../prescription/growth-data.js', import.meta.url),
    banner + 'window.WHO_GROWTH = ' + JSON.stringify(WHO_GROWTH) + ';\n');
  console.log('OK — wrote prescription/growth-data.js (anchors validated)');
}
main().catch((e) => { console.error(e); process.exit(1); });
